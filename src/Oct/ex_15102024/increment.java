package Oct.ex_15102024;

public class increment {
    public static void main(String[] args){
        //pre increment
        int a=10;
        int b=++a;
        System.out.println(a);
        System.out.println(b);
        //post increment
        int p=19;
        System.out.println(p);
        System.out.println(p++);
        int x=30;
        int y=x++;
        System.out.println(y);
        System.out.println(x);
    }
}
